    private void loadDataIntoTable() {
        String[] columnNames = {"Mentee Name", "Mentee Student ID", "Mentor Name"};
        DefaultTableModel model = new DefaultTableModel(columnNames, 0);
        
        try (Connection connection = DriverManager.getConnection(URL, USER, PASSWORD);
             Statement statement = connection.createStatement()) {
             
            String query = "SELECT menteename, menteestudentid, mentorname FROM mentormentee";
            ResultSet resultSet = statement.executeQuery(query);
            
            while (resultSet.next()) {
                String menteeName = resultSet.getString("menteename");
                String menteeStudentId = resultSet.getString("menteestudentid");
                String mentorName = resultSet.getString("mentorname");
                
                model.addRow(new Object[]{menteeName, menteeStudentId, mentorName});
            }
            
            jTable1.setModel(model);
            
        } catch (SQLException e) {
            e.printStackTrace();
        }
    }
